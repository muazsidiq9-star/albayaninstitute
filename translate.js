function translate(lang) {
  currentLang = lang;
  localStorage.setItem("lang", lang);

  // =========================
  // TEXT ELEMENTS
  // =========================
  document.querySelectorAll(
    "[data-i18n], i, p, h1, h2, h3, h4, span, a, li, button, label"
  ).forEach(el => {
if (el.dataset.noTranslate === "true") return;
if (el.dataset.i18n) {
  const key = el.dataset.i18n;
  if (!el.dataset.original) el.dataset.original = el.textContent.trim();
  el.textContent = (lang === "ar")
    ? (DICT.ar?.[key] || DICT[key] || el.dataset.original)
    : el.dataset.original;
  return;
}
    if (
      el.childNodes.length === 1 &&
      el.childNodes[0].nodeType === Node.TEXT_NODE
    ) {
      const original = el.dataset.original || el.textContent.trim();
      el.dataset.original = original;

      el.textContent = (lang === "ar")
        ? (DICT[original] || original)
        : original;
    }
  });

  // =========================
  // INPUT PLACEHOLDERS (FIXED)
  // =========================
  document.querySelectorAll("input[placeholder], textarea[placeholder]").forEach(el => {

    if (!el.dataset.originalPlaceholder) {
      el.dataset.originalPlaceholder = el.placeholder;
    }

    const base = el.dataset.originalPlaceholder;

    el.placeholder = (lang === "ar")
      ? (DICT[base] || base)
      : base;
  });

  // =========================
  // SELECT OPTIONS
  // =========================
  document.querySelectorAll("select").forEach(select => {
    [...select.options].forEach(opt => {

      if (!opt.dataset.original) {
        opt.dataset.original = opt.textContent.trim();
      }

      opt.textContent = (lang === "ar")
        ? (DICT[opt.dataset.original] || opt.dataset.original)
        : opt.dataset.original;
    });
  });

  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  const btn = document.getElementById("langToggleBtn");
  if (btn) btn.textContent = lang === "en" ? "AR" : "EN";
}

